// HERE WE WILL LEARN LOOP FUNCTION


#include<iostream>
using namespace std;

int main(int argc, char const *argv[])
{
    int i=1;
    
    cout<<i<<endl;
    i++; /*here i++ = 1*/
    cout<<i<<endl;
    i++; /*here the value is 1 already. and additional + here makes it 2*/
    cout<<i<<endl;
    i++; /*here the value is 2 already. and additional + here makes it 3. And so on for other logics below*/
    cout<<i<<endl;
    i++;
    cout<<i<<endl;
    i++;
    cout<<i<<endl;
    i++;
    cout<<i<<endl;
    i++;
    cout<<i<<endl;
    i++;
    cout<<i<<endl;
    i++;
    cout<<i<<endl;
    i++;
    
    /* 
    as  i have written this fincion 11 times, it will keep printing the
    value of "1" 10 times.    
    */
    return 0;



}

// USING maultiple FOR LOOP


#include<iostream>;
using namespace std;

int main()
{
     for (float number = 1.11f; number <= 5.11f; number++)
     {
        cout<<number<<endl;
     }

     cout<<endl;

     for (int i = 1; i <= 5; i++)
     {
        cout<<i<<endl;
     }

     cout<<endl;

     for (char alphabets = 'A'; alphabets <= 'Z'; alphabets++)
     {
        cout<<alphabets;
     }
        
     
     
return 0;
}